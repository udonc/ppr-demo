export const getProductById = (id: string): Product | undefined => {
  return products.find(product => product.id === id);
};

export const getProductsByCategory = (category: string): Product[] => {
  return products.filter(product => product.category === category);
};

export const getAllCategories = (): string[] => {
  return Array.from(new Set(products.map(product => product.category)));
};

// 動的データ取得関数（実際のAPIコールをシミュレート）
export const getProductPrice = async (id: string): Promise<number> => {
  // APIコールの遅延をシミュレート
  await new Promise(resolve => setTimeout(resolve, 500));
  
  const product = getProductById(id);
  if (!product) {
    throw new Error('Product not found');
  }
  
  // 実際の実装では外部APIから最新価格を取得
  // ここではランダムな変動をシミュレート（±5%）
  const variation = 0.95 + Math.random() * 0.1;
  return Math.round(product.price * variation);
}

export const getProductStock = async (id: string): Promise<number> => {
  // APIコールの遅延をシミュレート
  await new Promise(resolve => setTimeout(resolve, 300));
  
  const product = getProductById(id);
  if (!product) {
    throw new Error('Product not found');
  }
  
  // 実際の実装では外部APIから最新在庫を取得
  // ここではランダムな在庫数をシミュレート
  return Math.max(0, product.stock - Math.floor(Math.random() * 5));
}

// おすすめ商品を取得（現在の商品と同じカテゴリからランダムに選択）
export const getRecommendedProducts = async (
  currentProductId: string,
  limit: number = 4
): Promise<Product[]> => {
  // APIコールの遅延をシミュレート
  await new Promise(resolve => setTimeout(resolve, 2000));
  
  const currentProduct = getProductById(currentProductId);
  if (!currentProduct) {
    return [];
  }
  
  // 同じカテゴリの商品を取得（現在の商品を除く）
  const categoryProducts = products.filter(
    p => p.category === currentProduct.category && p.id !== currentProductId
  );
  
  // ランダムにシャッフル
  const shuffled = [...categoryProducts].sort(() => Math.random() - 0.5);
  
  // 指定された数だけ返す
  return shuffled.slice(0, Math.min(limit, shuffled.length));
}
